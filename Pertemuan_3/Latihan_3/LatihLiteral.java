public class LatihLiteral {
    public static void main(String[] args) {
        byte a = 127;
        short b = 2026;
        int c = 275000000;
        long d = 8000000000L;
        float e = 3.14f;
        double f = 2.718281828;
        char g = 'J';
        boolean h = true;

        System.out.println("=== DEMONSTRASI LITERAL TIPE DATA ===");    
        System.out.println("Tipe byte   : " + a);    
        System.out.println("Tipe short  : " + b);    
        System.out.println("Tipe int    : " + c);    
        System.out.println("Tipe long   : " + d);    
        System.out.println("Tipe float  : " + e);    
        System.out.println("Tipe double  : " + f);    
        System.out.println("Tipe char  : " + g);    
        System.out.println("Tipe boolean  : " + h);    
        
    }
}
